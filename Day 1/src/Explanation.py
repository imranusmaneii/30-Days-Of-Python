# 2. Checking Data Types

print(type(2))#                                 --->  Int
print(type(3.14))#                              --->  Double
print(type(1 +3j))#                             --->  Complex
print(type('Imran'))#                           --->  String
print(type([1, 2, 3]))#                    --->  List
print(type({'nam' : 'ImranUsmani'}))# --->  Dictionary
